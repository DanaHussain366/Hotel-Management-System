private void loadAvailableRooms() {

    cmbRoomNumber.removeAllItems();

    for (Room room : roomService.getAvailableRooms()) {
        cmbRoomNumber.addItem(
                String.valueOf(room.getRoomNumber())
        );
    }
}

private void refreshTable() {

    DefaultTableModel model =
            (DefaultTableModel) tblReservations.getModel();

    model.setRowCount(0);

    for (Reservation reservation : manager.getReservations()) {

        model.addRow(new Object[]{
            reservation.getReservationId(),
            reservation.getGuestName(),
            reservation.getGuestPhone(),
            reservation.getGuestEmail(),
            reservation.getPassportNumber(),
            reservation.getRoomNumber(),
            reservation.getCheckInDate(),
            reservation.getCheckOutDate(),
            reservation.calculateNumberOfNights(),
            reservation.getReservationStatus()
        });
    }
}
