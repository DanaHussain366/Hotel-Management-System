    private void btnCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckOutActionPerformed
        try {

    int reservationId =
            Integer.parseInt(txtReservationId.getText());

    Reservation reservation =
            manager.searchById(reservationId);

    if (reservation == null) {

        JOptionPane.showMessageDialog(
                this,
                "Reservation not found."
        );

        return;
    }

    if (reservation.getReservationStatus()
            != ReservationStatus.CHECKED_IN) {

        JOptionPane.showMessageDialog(
                this,
                "Only a checked-in guest can check out."
        );

        return;
    }

    if (!roomService.releaseRoom(
            reservation.getRoomNumber())) {

        JOptionPane.showMessageDialog(
                this,
                "The room could not be released."
        );

        return;
    }

    manager.checkInCheckOut(
            reservationId,
            ReservationStatus.CHECKED_OUT
    );

    files.saveReservations();
    refreshTable();
    loadAvailableRooms();

    JOptionPane.showMessageDialog(
            this,
            "Guest checked out successfully."
    );

} catch (NumberFormatException e) {

    JOptionPane.showMessageDialog(
            this,
            "Enter a valid reservation ID."
    );
}
    }//GEN-LAST:event_btnCheckOutActionPerformed

