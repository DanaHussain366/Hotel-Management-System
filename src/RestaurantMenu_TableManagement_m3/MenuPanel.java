    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            int itemId = Integer.parseInt(txtItemID.getText().trim());
            String itemName = txtItemName.getText().trim();
            String category = cmbCategory.getSelectedItem().toString();
            double price = Double.parseDouble(txtPrice.getText().trim());
            int preparationTime = Integer.parseInt(txtPreparationTime.getText().trim());
            boolean available = chkAvailable.isSelected();
            
            if (itemName.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter the item name.");
            return;
            }
            
            if (price <= 0){
            JOptionPane.showMessageDialog(this, "Price must be greater than 0.");
            return;
            }
            
            if (preparationTime <= 0){
            JOptionPane.showMessageDialog(this, "Preparation time must be greater than 0.");
            return;
            }
            
            if (menuManager.searchItem(itemId) != null) {
            JOptionPane.showMessageDialog(this, "Item ID already exists.");
            return;
            }
            
            MenuItem item = new MenuItem( itemId, itemName, category, price, preparationTime, available );
            
            menuManager.addMenuItem(item);
            
            fileHandler.saveMenu(menuManager.getAllMenuItems());
            
            refreshTable();
            
            clearFields();
            
            JOptionPane.showMessageDialog(this, "Menu item added successfully!");
            
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values.");
        } 
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            int itemId = Integer.parseInt(txtItemID.getText().trim());
            
            MenuItem item = menuManager.searchItem(itemId);
            
            if (item == null){
                JOptionPane.showMessageDialog(this, "Item not found.");
                return;
            }
            
            item.setItemName(txtItemName.getText().trim());
            item.setCategory(cmbCategory.getSelectedItem().toString());
            item.setPrice(Double.parseDouble(txtPrice.getText().trim()));
            item.setPreparationTime(Integer.parseInt(txtPreparationTime.getText().trim()));
            item.setAvailable(chkAvailable.isSelected());
            
            fileHandler.saveMenu(menuManager.getAllMenuItems());
            
            refreshTable();
            clearFields();
            
            JOptionPane.showMessageDialog(this, "Menu item updated successfully!");
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values.");
        }   

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
            int row = tblMenu.getSelectedRow();
            
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Please select an item.");
                return;
            }
            
            int choice = JOptionPane.showConfirmDialog( this, "Are you sure you want to delete this item?","Delete Item",JOptionPane.YES_NO_OPTION);
            
            if (choice == JOptionPane.YES_OPTION) {
                menuManager.removeItem(row);
                DefaultTableModel model = (DefaultTableModel) tblMenu.getModel(); 
                
                refreshTable();
                fileHandler.saveMenu(menuManager.getAllMenuItems());
                clearFields(); 
                JOptionPane.showMessageDialog(this, "Item deleted successfully.");
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

