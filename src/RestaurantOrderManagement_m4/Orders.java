    private double getPrice() {

    switch (jComboBox1.getSelectedIndex()) {

        case 0: return 2.5;   // Caesar Salad
        case 1: return 1.5;   // Garlic Bread
        case 2: return 4.5;   // Chicken Burger
        case 3: return 5.0;   // Beef Burger
        case 4: return 6.5;   // Grilled Chicken
        case 5: return 5.5;   // Margherita Pizza
        case 6: return 5.75;  // Pasta Alfredo
        case 7: return 1.75;  // French Fries
        case 8: return 2.0;   // Onion Rings
        case 9: return 3.0;   // Chocolate Cake
        case 10: return 2.5;  // Ice Cream
        case 11: return 1.25; // Soft Drink
        case 12: return 1.5;  // Fresh Juice
        case 13: return 2.5;  // Orange Juice
        case 14: return 3.75; // Chicken Wings
        case 15: return 3.5;  // Cheesecake

        default: return 0;
    }
}
    
    
    private void updateTotal() {

    int quantity = (Integer) jSpinner1.getValue();

    double price = getPrice();

    jTextField1.setText(String.format("%.2f", price));

    double total = price * quantity;

    jTextField4.setText(String.format("%.2f", total));
}
    
    
   private void updateOrderTypePanels() {

    String type =
            jComboBox2.getSelectedItem().toString();

    if (type.equals("Room Service")) {

        jPanel1.setVisible(true);
        jPanel2.setVisible(false);

    } else if (type.equals("Dine In")) {

        jPanel1.setVisible(false);
        jPanel2.setVisible(true);

    } else {

        jPanel1.setVisible(false);
        jPanel2.setVisible(false);
    }

    revalidate();
    repaint();
    pack();
}

