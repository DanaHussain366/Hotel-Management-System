    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String selectedType =
            jComboBox2.getSelectedItem().toString();

    OrderType orderType;

    if (selectedType.equals("Room Service")) {

        orderType = OrderType.ROOM_SERVICE;

    } else if (selectedType.equals("Dine In")) {

        orderType = OrderType.DINE_IN;

    } else {

        orderType = OrderType.TAKEAWAY;
    }

    int roomNumber = 0;
    int tableNumber = 0;

    if (orderType == OrderType.ROOM_SERVICE) {

        String roomText =
                jTextField2.getText().trim();

        if (roomText.isEmpty()) {

            JOptionPane.showMessageDialog(
                    this,
                    "Enter the room number."
            );

            return;
        }

        try {

            roomNumber =
                    Integer.parseInt(roomText);

            if (roomNumber <= 0) {

                JOptionPane.showMessageDialog(
                        this,
                        "Room number must be greater than zero."
                );

                return;
            }

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(
                    this,
                    "Room number must be a valid number."
            );

            return;
        }
    }

    if (orderType == OrderType.DINE_IN) {

        String tableText =
                jTextField3.getText().trim();

        if (tableText.isEmpty()) {

            JOptionPane.showMessageDialog(
                    this,
                    "Enter the table number."
            );

            return;
        }

        try {

            tableNumber =
                    Integer.parseInt(tableText);

            if (tableNumber <= 0) {

                JOptionPane.showMessageDialog(
                        this,
                        "Table number must be greater than zero."
                );

                return;
            }

        } catch (NumberFormatException ex) {

            JOptionPane.showMessageDialog(
                    this,
                    "Table number must be a valid number."
            );

            return;
        }
    }

    int orderId =
            RestaurantData.orderManager
                    .getOrders()
                    .size() + 1;

    int itemId =
            jComboBox1.getSelectedIndex() + 1;

    String itemName =
            jComboBox1.getSelectedItem().toString();

    int quantity =
            (Integer) jSpinner1.getValue();

    double pricePerItem =
            getPrice();

    double totalAmount =
            quantity * pricePerItem;

    RestaurantOrder order =
            new RestaurantOrder(
                    orderId,
                    orderType,
                    roomNumber,
                    tableNumber,
                    "Guest",
                    itemId,
                    itemName,
                    quantity,
                    pricePerItem,
                    LocalDate.now().toString(),
                    OrderStatus.PLACED
            );

    RestaurantData.orderManager.addOrder(order);

    JOptionPane.showMessageDialog(
            this,
            "Order placed successfully!\n"
            + "Order ID: " + orderId
            + "\nOrder Type: " + selectedType
            + "\nItem: " + itemName
            + "\nPrice Per Item: "
            + String.format("%.2f", pricePerItem)
            + "\nQuantity: " + quantity
            + "\nTotal Amount: "
            + String.format("%.2f", totalAmount)
    );

    jComboBox2.setSelectedIndex(0);
    jComboBox1.setSelectedIndex(0);
    jSpinner1.setValue(1);

    jTextField2.setText("");
    jTextField3.setText("");

    updateOrderTypePanels();
    updateTotal();
    }//GEN-LAST:event_jButton2ActionPerformed

